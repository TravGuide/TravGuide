<?php
$name = $_POST['name'];
$email  = $_POST['email'];
$subject  = $_POST['subject'];
$message = $_POST['message'];


$connect = new mysqli('localhost','root','','tesdb');

if($connect->connect_error){
    
    die('Connection Failed : '.$connect->connect_error);
    
}else{
    
    $stmt = $connect->prepare("insert into form(name,email,subject,message) values(?,?,?,?)");
    $stmt->bind_param("ssss",$name,$email,$subject,$message);
    $stmt->execute();
    $stmt->close();
    $connect->close();
    
    
}
